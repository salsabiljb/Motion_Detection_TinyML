static int intToStr(int16_t x, char str[])
{
    int i = 0;
    if(x == 0)
    {
    	str[0] = '0';
    	str[1] = '\0';
    	i = 1;
    }
    else
    {
      if(x<0)
       {
    	i=1;
    	str[0] = '-';
    	x *= -1;
       }
      while (x)
      {
        str[i++] = (x % 10) + '0';
         x = x / 10;
      }
      if(str[0] == '-')
      {
    	  reverse(&str[1], i-1);
      }
      else
      {
    	  reverse(str, i);
      }
      str[i] = '\0';
    }

    return i;
}
// Reverses a string 'str' of length 'len'
static void reverse(char* str, int len)
{
    int i = 0, j = len - 1, temp;
    while (i < j) {
        temp = str[i];
        str[i] = str[j];
        str[j] = temp;
        i++;
        j--;
    }
}



